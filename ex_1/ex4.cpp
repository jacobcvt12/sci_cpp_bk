#include <iostream>

int main()
{
	// initialize matrix variables
	double A[2][2] = { {1.0, 3.0}, {2.5, 3.7} };
	double B[2][2] = { {4.7, 3.8}, {6.9, 1.3} };
	double C[2][2], D[2][2];

	C[0][0] = A[0][0] + B[0][0];
	C[0][1] = A[0][1] + B[0][1];
	C[1][0] = A[1][0] + B[1][0];
	C[1][1] = A[1][1] + B[1][1];

	D[0][0] = A[0][0] * B[0][0] + A[0][1] * B[1][0];
	D[0][1] = A[0][0] * B[0][1] + A[0][1] * B[1][1];
	D[1][0] = A[1][0] * B[0][0] + A[1][1] * B[1][0];
	D[1][0] = A[1][0] * B[0][1] + A[1][1] * B[1][1];

	return 0;
}


